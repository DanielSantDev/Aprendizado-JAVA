package b_fundamentos;

public class F_OpUnarios {

	public static void main(String[] args) {
		
		int a = 1;
		int b = 2;
		
		a++; // a + 1
		a--; // a - 1
		
		++b; // b + 1
		--b; // b - 1
		
		System.out.println(++a == b--);
		System.out.println(a == b);
		System.out.println(a);
		System.out.println(b);
		
	}
}
